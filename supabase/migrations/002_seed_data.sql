-- ==================== PRICES (COP) ====================

-- Pizzas: mismo precio por tamaño para todas
INSERT INTO product_prices (product_id, size, price)
SELECT p.id, s.size, s.price
FROM products p
CROSS JOIN (VALUES
  ('INDIVIDUAL'::pizza_size, 5000),
  ('DIAMANTE'::pizza_size, 9000),
  ('MEDIANA'::pizza_size, 17000),
  ('FAMILIAR'::pizza_size, 32000)
) AS s(size, price)
WHERE p.category = 'PIZZA';

